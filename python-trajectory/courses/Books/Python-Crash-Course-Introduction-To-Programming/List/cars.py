
# organizing lists

cars = [ 'bmw', 'audi', 'toyota', 'subaru']
cars.sort()
print(cars)



cars = [ 'bmw', 'audi', 'toyota', 'subaru']
cars.sort(reverse=True)
print(cars)


# sorting a list temporaly  with the sorted() function

cars = [ 'bmw', 'audi', 'toyota', 'subaru']
print('Here is the original list:')
print(cars)

print('\nHere is the sorted list:')
print(sorted(cars))

print('\nHere is the original list again:')
print(cars)


cars = [ 'bmw', 'audi', 'toyota', 'subaru']
cars.sort(reverse=True)
print(cars)


# printing a list in reverse oder 

cars = [ 'bmw', 'audi', 'toyota', 'subaru']
print(cars)

cars.reverse()
print(cars)



